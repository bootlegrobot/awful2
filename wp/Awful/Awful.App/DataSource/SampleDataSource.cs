    /// <summary>
    /// Interface for objects that can be represented by a unique identifier.
    /// </summary>
    public interface IUnique
    {
        string UniqueId { get; }
    }

    /// <summary>
    /// Interface for items that can be added or removed from a pinned items list.
    /// </summary>
    public interface IPinnable : IUnique
    {
        bool IsPinned { get; set; }
    }

    /// <summary>
    /// Base class for <see cref="CommonDataItem"/> and <see cref="CommonDataGroup"/> that
    /// defines properties common to both.
    /// </summary>
    [DataContract]
    public abstract class CommonDataObject : Awful.Common.BindableBase, IUnique
    {
        //private static Uri _baseUri = new Uri("ms-appx:///");

        public CommonDataObject(String uniqueId, String title, String subtitle, String imagePath, String description)
        {
            this._uniqueId = uniqueId;
            this._title = title;
            this._subtitle = subtitle;
            this._description = description;
            this._imagePath = imagePath;
        }
		
		public CommonDataObject() : this(null, null, null, null, null) { }

        private string _uniqueId = string.Empty;
        [DataMember]
        public string UniqueId
        {
            get { return this._uniqueId; }
            set { this.SetProperty(ref this._uniqueId, value, "UniqueId"); }
        }

        private string _title = string.Empty;
        [DataMember]
        public string Title
        {
            get { return this._title; }
            set { this.SetProperty(ref this._title, value, "Title"); }
        }

        private string _subtitle = string.Empty;
        [DataMember]
        public string Subtitle
        {
            get { return this._subtitle; }
            set { this.SetProperty(ref this._subtitle, value, "Subtitle"); }
        }

        private string _description = string.Empty;
        [DataMember]
        public string Description
        {
            get { return this._description; }
            set { this.SetProperty(ref this._description, value, "Description"); }
        }

        private ImageSource _image = null;
        private String _imagePath = null;

        [IgnoreDataMember]
        public ImageSource Image
        {
            get
            {
                if (this._image == null && this._imagePath != null)
                {
                    this._image = new BitmapImage(new Uri(this._imagePath, UriKind.RelativeOrAbsolute));
                }
                return this._image;
            }

            set
            {
                this._imagePath = null;
                this.SetProperty(ref this._image, value, "Image");
            }
        }

        [DataMember]
        public string ImagePath
        {
            get { return this._imagePath; }
            set { SetImage(value); }
        }

        public void SetImage(String path)
        {
            this._image = null;
            this._imagePath = path;
            this.OnPropertyChanged("Image");
            this.OnPropertyChanged("ImagePath");
        }

        public override string ToString()
        {
            return this.Title;
        }
    }

